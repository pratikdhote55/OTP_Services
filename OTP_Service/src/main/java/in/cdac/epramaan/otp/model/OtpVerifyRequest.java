package in.cdac.epramaan.otp.model;

import java.util.UUID;

public class OtpVerifyRequest {

	private String dept_id;
	private UUID txn_id;
	private String otp_value;
	private long timestamp;
	private int otp_as_hash;
	
	public OtpVerifyRequest(){
		otp_as_hash = 0;
	}

	public String getDept_id() {
		return dept_id;
	}

	public void setDept_id(String dept_id) {
		this.dept_id = dept_id;
	}

	public UUID getTxn_id() {
		return txn_id;
	}

	public void setTxn_id(UUID txn_id) {
		this.txn_id = txn_id;
	}

	public long getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(long timestamp) {
		this.timestamp = timestamp;
	}

	public String getOtp_value() {
		return otp_value;
	}

	public void setOtp_value(String otp_value) {
		this.otp_value = otp_value;
	}

	public int getOtp_as_hash() {
		return otp_as_hash;
	}

	public void setOtp_as_hash(int otp_as_hash) {
		this.otp_as_hash = otp_as_hash;
	}	
}